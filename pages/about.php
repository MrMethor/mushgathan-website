      The song can be described as very repetitive and unusual, almost experimental.</p>
    <p class="about-bio-text">Next steps of the band were on their standard path with a thrashy song called "Soft". It was mainly composed by the lead guitarist Filip and the drummer Samuel.
      They experimented with something completely new, a punk-rock parody ironically called "Punk Is Now Dead", composed by Jakub and Samuel in a few minutes.
      From there their journey continued without any major additions to their discography, until Samuel decided to create his own short song called "Inner Peace".
      It is an unusual song with very natural and calm feel.</p>
